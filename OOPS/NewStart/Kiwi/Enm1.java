enum Status{
    Running,Pending,Directing,Completed,Failed; 
}
public class Enm1 {
    public static void main(String[] args) {
        Status s=Status.Running;

        switch (s) {
            case Running:
                System.out.println("The program is under execution");
                break;

            case Pending:
                System.out.println("The program execution is not yet completed");
                break;

            case Directing:
                System.out.println("The program is being directed to the server");
                break;

            case Completed:
                System.out.println("The execution of this program is completed");
                break;    

            default: 
            System.out.println("The program execution failed");
                break;
        }
    }
}
