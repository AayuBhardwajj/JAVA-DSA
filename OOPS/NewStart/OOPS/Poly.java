class A{
    int add(int a, int b){
        return a+b;
    }
    float add(float a, float b){
        return a+b;
    }
}
public class Poly{
    public static void main(String[] args) {
        A a= new A();
        System.out.println("The integer sum of '2' and '5' is: "+a.add(2, 5));
        System.out.println("The float sum of '2' and '5' is: "+a.add(2.0f, 5.0f));
    }
}

    

